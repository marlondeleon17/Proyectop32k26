    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
int codigoBusqueda = Integer.parseInt(txtBuscar.getText()); // Obtiene el ID del campo de texto
UsuarioDAO dao = new UsuarioDAO(); // Instancia el acceso a datos
Usuario user = dao.buscar(codigoBusqueda); // Ejecuta el método de búsqueda

if (user != null) {
    // Si el usuario existe, llena todos los campos de texto con sus datos
    txtCodigo.setText(String.valueOf(user.getCodigo())); 
    txtNombre.setText(user.getNombre());
    txtCorreo.setText(user.getCorreo());
    txtTelefono.setText(user.getTelefono());
    txtDireccion.setText(user.getDireccion());
    txtContraseña.setText(user.getContrasena());
    txtEstado.setText(user.getEstado());
} else {
    // Si no existe, muestra un mensaje de advertencia
    JOptionPane.showMessageDialog(this, "Usuario no encontrado");
}
    }//GEN-LAST:event_btnBuscarActionPerformed

