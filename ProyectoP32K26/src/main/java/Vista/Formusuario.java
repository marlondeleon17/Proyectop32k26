     private void listarUsuarios() {
    UsuarioDAO dao = new UsuarioDAO();
    List<Usuario> lista = dao.listar(); // Obtiene la lista actualizada de la DB
    DefaultTableModel modelo = (DefaultTableModel) tablaUsuario.getModel();
    modelo.setRowCount(0); // Limpia las filas actuales para evitar duplicados al recargar
    
    Object[] objeto = new Object[7]; // Arreglo para mapear los datos del objeto a la fila
    for (Usuario u : lista) {
        objeto[0] = u.getCodigo();
        objeto[1] = u.getNombre();
        objeto[2] = u.getCorreo();
        objeto[3] = u.getTelefono();
        objeto[4] = u.getDireccion();
        objeto[5] = u.getContrasena();
        objeto[6] = u.getEstado();
        modelo.addRow(objeto); // Agrega la fila a la tabla visual
    }
}
